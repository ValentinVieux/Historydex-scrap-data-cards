function main() {
  ensureDir(PATHS.approved);
  let written = 0;
  let failed = 0;
  for (const seed of SEEDS) {
    try {
      const card = buildCard(seed);
      const validated = CardSchema.parse(card);
      const out = path.join(PATHS.approved, `${validated.id}.json`);
      writeJson(out, validated);
      written++;
    } catch (err) {
      console.error(`Échec carte ${seed.id} (${seed.dexNum}): ${(err as Error).message}`);
      failed++;
    }
  }
  console.log(`Cartes écrites : ${written}/${SEEDS.length}`);
  if (failed > 0) {
    console.error(`Échecs : ${failed}`);
    process.exit(1);
  }
}

main();
