function buildCard(seed: CardSeed): Card {
  const defaultPrompt = VERB_BY_PLACEKIND[seed.placeKind] ?? VERB_BY_PLACEKIND.other!;
  const wherePrompt: WherePrompt = {
    pre: seed.wherePre ?? defaultPrompt.pre,
    verb: seed.whereVerb ?? defaultPrompt.verb,
    post: seed.wherePost ?? defaultPrompt.post,
  };

  const card: Card = {
    id: seed.id,
    dexNum: seed.dexNum,
    canonical: {
      subjectKey: seed.subjectKey,
      type: seed.type,
      aliases: seed.aliases,
      factNotes: seed.factNotes ?? [],
      wikipediaTitle: WIKIPEDIA_TITLES[seed.dexNum] ?? null,
      time: {
        tag: seed.tag,
        timeKind: seed.timeKind,
        pivotYear: seed.pivotYear,
        startYear: seed.startYear ?? null,
        endYear: seed.endYear ?? null,
        justification: seed.timeJustification,
      },
      place: {
        placeKind: seed.placeKind,
        placeCanonicalName: seed.placeCanonicalName,
        lat: seed.lat,
        lon: seed.lon,
        region: seed.region,
        countryCode: seed.countryCode,
        geoKind: "earth",
        justification: seed.placeJustification,
      },
    },
    gameplay: {
      era: seed.era,
      whenDelta: seed.whenDelta,
      whereRadiusKm: seed.whereRadiusKm,
      difficultyWhen: seed.difficultyWhen,
      difficultyWhere: seed.difficultyWhere,
      eligibleForWhen: true,
      eligibleForWhere: true,
      balanceNotes: "",
    },
    display: {
      defaultLocale: "fr",
      locales: {
        fr: {
          title: seed.title,
          blurb: seed.blurb,
          body: seed.body,
          placeLabel: seed.placeLabel,
          timeDisplayLabel: seed.timeDisplayLabel,
          wherePrompt,
        },
        en: null,
      },
      imageLabel: seed.imageLabel,
      translationNotes: [],
    },
    editorial: {
      status: "approved",
      confidence: "medium",
      contentVersion: 1,
      notes: [TEST_NOTE, ...(seed.notes ?? [])],
      warnings: seed.warnings ?? [],
      sources: seed.sources.map((s) => ({
        title: s.title,
        url: s.url,
        publisher: s.publisher,
        author: s.author ?? null,
        accessedAt: s.accessedAt ?? ACCESSED,
        relevance: s.relevance,
        quote: s.quote,
      })),
    },
  };
  return card;
}

